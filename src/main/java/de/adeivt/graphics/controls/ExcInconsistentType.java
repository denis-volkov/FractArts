package de.adeivt.graphics.controls;

public class ExcInconsistentType extends Exception {

	public ExcInconsistentType() {
		super();
	}

	public ExcInconsistentType(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ExcInconsistentType(String message, Throwable cause) {
		super(message, cause);
	}

	public ExcInconsistentType(String message) {
		super(message);
	}

	public ExcInconsistentType(Throwable cause) {
		super(cause);
	}
	 
}
